#include <unistd.h>

int	main(int arc, char *arv[])
{
	int	i[2];

	i[1] = arc - 1;
	while (i[1] >= 1)
	{
		i[0] = 0;
		while (arv[i[1]][i[0]])
		{
			write(1, &arv[i[1]][i[0]], 1);
			i[0]++;
		}
		write(1, "\n", 1);
		i[1]--;
	}
	return (0);
}
